export type LocalizedStrings = (typeof stringsByLocale)[SupportedLocale];

export function getCurrentLocale(): SupportedLocale {
  const appLanguage = typeof getLanguage === 'function' ? getLanguage() : 'en';
  return appLanguage.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

export function getLocalizedStrings(): LocalizedStrings {
  return stringsByLocale[getCurrentLocale()];
}
